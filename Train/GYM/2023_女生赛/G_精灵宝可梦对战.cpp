void solve() {
    int n, m, k, cnt = 0, flag = 0;
    cin>> n >> m >> k;
    queue<Zhenen> a, b;
    for(int i = 0; i < n; ++ i) {
        Zhenen l;
        cin>> l.h >> l.a >> l.b >> l.c >> l.d >> l.e >> l.w;
        a.push(l);
    }
    for(int i = 0; i < m; ++ i) {
        Zhenen l;
        cin>> l.h >> l.a >> l.b >> l.c >> l.d >> l.e >> l.w;
        b.push(l);
    }
    Zhenen A = a.front(), B = b.front();
    a.pop(), b.pop();
    while(cnt < k) {
        if(A.te >= A.e && A.w > max(A.a - B.c, 0) && A.w > max(A.b - B.d, 0)) 
            A.te -= A.e, B.h -= A.w, a.push(A);
        else B.h -= max(max(A.a - B.c, 0), max(A.b - B.d, 0)), ++ A.te, a.push(A);
        A = a.front();
        a.pop();
        if(B.h <= 0) {
            if(b.empty()) {
                flag = 1;
                break;
            }
            B = b.front();
            b.pop();
        }
        if(B.te >= B.e && B.w > max(B.a - A.c, 0) && B.w > max(B.b - A.d, 0)) 
            B.te -= B.e, A.h -= B.w, b.push(B);
        else A.h -= max(max(B.a - A.c, 0), max(B.b - A.d, 0)), ++ B.te, b.push(B);
        B = b.front();
        b.pop();
        if(A.h <= 0) {
            if(a.empty()) {
                flag = 2;
                break;
            }
            A = a.front();
            a.pop();
        }
        ++ cnt;
    }
    if(flag == 0) cout << "Draw" <<endl;
    else if(flag == 1) cout << "Alice" <<endl;
    else cout << "Bob" <<endl;
} 
