vector<int> cmp(vector<int> a, vector<int> b) {
    if(a.empty()) return b;
    if(b.empty()) return a;
    if(a.size() > b.size()) return a;
    if(a.size() < b.size()) return b;
    if(a.size() == b.size()) {
        for(int i = 0; i < (int)a.size(); ++ i) {
            if(a[i] > b[i]) return a;
            if(a[i] < b[i]) return b;
        }
    }
    return a;
}

