struct SEGT {
    #define ls id * 2
    #define rs id * 2 + 1

    vector<int> a, seg;

    SEGT(int n) : a(n + 5), seg(n * 4) {}

    void update(int id) {
        seg[id] = max(seg[ls], seg[rs]);
    }

    void build(int id, int l, int r) {
        if(l == r) seg[id] = a[l];
        else {
            int mid = l + r >> 1;
            build(ls, l, mid);
            build(rs, mid + 1, r);
            update(id);
        }
    }

    void change(int id, int l, int r, int k, int v) {
        if(l == r) {
            seg[id] = v;
            return;
        }else {
            int mid = l + r >> 1;
            if(k <= mid) change(ls, l, mid, k, v);
            else change(rs, mid + 1, r, k, v);
            update(id);
        }
    }

    int query(int id, int l, int r, int ql, int qr) {
        if(ql == l && qr == r) return seg[id];
        else {
            int mid = l + r >> 1;
            if(qr <= mid) return query(ls, l, mid, ql, qr);
            else if(ql > mid) return query(rs, mid + 1, r, ql, qr);
            else return max(query(ls, l, mid, ql, mid), query(rs, mid + 1, r, mid + 1, qr));
        }
    }

    #undef ls
    #undef rs
};

void solve(int n, int m);
