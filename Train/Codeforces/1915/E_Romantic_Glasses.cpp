void solve() {
    int n, d = 0, o = 0;
    cin>> n;
    SEGT tr(n);
    vector<int> a(n + 1);
    for(int i = 1; i <= n; ++ i) {
        cin>> a[i];
        tr.a[i] = a[i];
        if(i & 1) d += a[i];
        else o += a[i];
    }
    if(o == d) {
        cout << "YES" <<endl;
        return;
    }
    for(int i = 1, j = n; i < j && o != d;) {
        if(o > d) {
            if(!(i & 1) && j & 1) o -= a[i ++];
            else if(!(j & 1) && i & 1) o -= a[j --];
            else if(!(j & 1) && !(i & 1)) a[j] < a[i] ? o -= a[j --] : o -= a[i ++];
            else if(j & 1 && i & 1) a[j] > a[i] ? d -= a[j --] : d -= a[i ++];
        }else {
            if(!(i & 1) && j & 1) d -= a[j --];
            else if(!(j & 1) && i & 1) d -= a[i ++];
            else if(!(j & 1) && !(i & 1)) a[j] > a[i] ? o -= a[i ++] : o -= a[j --];
            else if(j & 1 && i & 1) a[j] < a[i] ?  d -= a[i ++] : d -= a[j --];
        }
        if(o == d) {
            cout << "YES" <<endl;
            return;
        }
    }
    cout << "NO" <<endl;
} 
